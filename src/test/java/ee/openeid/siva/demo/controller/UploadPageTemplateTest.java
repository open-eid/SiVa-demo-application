package ee.openeid.siva.demo.controller;

import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.AutoConfigureMockMvc;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.test.web.servlet.MockMvc;

import static org.hamcrest.Matchers.containsString;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.content;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

@SpringBootTest
@AutoConfigureMockMvc
public class UploadPageTemplateTest {

    @Autowired
    private MockMvc mockMvc;

    @Test
    void displayStartPageCheckPresenceOfUploadForm() throws Exception {
        mockMvc.perform(get("/"))
                .andExpect(status().isOk())
                .andExpect(content().contentType("text/html;charset=UTF-8"))
                .andExpect(content().string(containsString("<form")))
                .andExpect(content().string(containsString("id=\"siva-dropzone\"")))
                .andExpect(content().string(containsString("action=\"upload\"")))
                .andExpect(content().string(containsString("method=\"POST\"")))
                .andExpect(content().string(containsString("enctype=\"multipart/form-data\"")));
    }
}
